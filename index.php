<h1>UM POUCO SOBRE CHURRO<h1>
Churro é um alimento de origem ibérica, muito popular nos países latino-americanos.<br>
Este doce é preparado com massa à base de farinha de trigo e água, <br>
em formato cilíndrico e frito em óleo vegetal. Logo após, <br>
ele pode ser salpicado com uma camada de açúcar por fora (opcionalmente, também com canela).<br>
Sendo um doce originário de Portugal e Espanha, é bastante consumido nos países hispano-americanos,<br>
desde o México até a Argentina. Porém, nestes países, o churro geralmente não possui recheio, <br>
diferente de Portugal e Brasil, onde os churros possuem uma cavidade interna contendo doce de leite ou chocolate.<br>
No Uruguai, Argentina e outros países da América Latina, o churro também pode receber recheio. É comum, no Brasil e em Portugal, <br>
vendedores ambulantes em carrocinhas comercializarem o produto em praças e eventos a céu aberto, como feiras e festas municipais.<br>

A preparação do churro é feita com uma ferramenta na qual a massa fica armazenada e, através de um dispositivo de pressão,<br>
tal qual uma manivela, envia a massa para uma forma, que confere o aspecto estriado da massa com um canal central. Logo após, <br>
essa massa preparada é frita em óleo vegetal e deixada em repouso até a inserção do recheio e posterior consumo.<br>

A inserção do recheio é realizada com uma espécie de tubo, que despeja o recheio no interior do churro.
